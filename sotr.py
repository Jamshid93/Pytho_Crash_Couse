# bu yerda sort() methodi orqali elon qilingan listni elementlarini tarib asosida joylashtirishni ko'ramiz
# masaln alfavit asosida joylashtirih kerak bo'lsin
car=["bmw","audi","nexia","toyota","ford"]
car.sort()
print(car)

# lekin shu alfavit orqali saralangan listni teskari tomoda chiqarish reverse() methodi yordamida amalga oshiriladi
car.sort(reverse=True)
print(car)